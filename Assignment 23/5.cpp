// 5. Write a C++ program to calculate the volume of a cuboid.

#include<iostream>
using namespace std;
int main()
{
    int l,b,h;
    cout<<"Enter Length of Cuboid: ";
    cin>>l;
    cout<<"Enter Breadth of Cuboid: ";
    cin>>b;
    cout<<"Enter Height of Cuboid: ";
    cin>>h;
    cout<<"Volume of Cuboid is: "<<l*b*h<<" unit cube";
    return 0;

}